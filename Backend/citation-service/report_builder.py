        exc = c.get("excerpt") or {}
        lines.append(f"2.7 Pinpoint Citation: {c.get('pinpointCitation') or c.get('primaryCitation') or '—'}")
        lines.append(f"    Source Excerpt ({exc.get('para') or 'Para'}): {str(exc.get('text') or '—')[:400]}")
        treat = c.get("treatment") or {}
        lines.append(
            f"2.8 Subsequent Treatment: Followed={treat.get('followed', 0)} "
            f"Distinguished={treat.get('distinguished', 0)} Overruled={treat.get('overruled', 0)} "
            f"| {treat.get('note') or '—'}"
        )
        lines.append(f"2.9 Verification: {c.get('verificationStatusLabel') or c.get('verificationStatus') or '—'}")
        lines.append(f"2.10 Official Source Link: {c.get('officialSourceLink') or c.get('sourceUrl') or '—'}")
        imp_link = c.get("importSourceLink") or c.get("sourceUrl") or c.get("officialSourceLink")
        if imp_link and str(imp_link).strip() not in ("—", ""):
            lines.append(f"Data imported from: {imp_link}")
        orig_url = c.get("originalCourtCopyUrl") or ""
        if orig_url:
            lines.append(f"Original Court Copy (PDF): {orig_url}")
        frag      = c.get("ikFragment") or {}
        frag_text = frag.get("headline") or ""
        if frag_text:
            lines.append(f"IK Relevant Fragment: {frag_text[:300]}")
        cite_list = c.get("ikCiteList") or []
        if cite_list:
            lines.append(f"Cases Cited ({len(cite_list)}): " + "; ".join(x.get("title", "") for x in cite_list[:5]))
