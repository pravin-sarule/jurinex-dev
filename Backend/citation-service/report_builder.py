def _detect_argument_party(raw_text: str, query: str = "", explicit_perspective: str = "") -> str:
    """
    PartyArgumentTagger: detect which party primarily relied on this citation.

    Tag from judgment content first, not from the user's dropdown.
    explicit_perspective is only a weak tiebreaker when the text is genuinely
    ambiguous; it must not overwrite a clear content-based signal.

    Returns: 'appellant' | 'respondent' | 'court' | 'neutral'
    """
    # Normalise explicit_perspective aliases
    _exp = (explicit_perspective or "").lower().strip()
    if _exp in ("appellant", "petitioner", "plaintiff"):
        _exp = "appellant"
    elif _exp in ("respondent", "defendant", "state", "prosecution"):
        _exp = "respondent"
    elif _exp == "court":
        _exp = "court"
    else:
        _exp = ""

    # Query-level perspective detection (only when no explicit override)
    if not _exp and query:
        if _RE_QUERY_RESPONDENT.search(query):
            return "respondent"
        if _RE_QUERY_APPELLANT.search(query):
            return "appellant"
        if _RE_QUERY_COURT.search(query):
            return "court"

    # Sliding-window text analysis (first 10 000 chars of judgment body)
    text = (raw_text or "")[:10000]
    appellant_count  = len(_RE_APPELLANT.findall(text))  if text else 0
    respondent_count = len(_RE_RESPONDENT.findall(text)) if text else 0
    court_count      = len(_RE_COURT.findall(text))      if text else 0

    if not text:
        return _exp or "neutral"
    if court_count > (appellant_count + respondent_count) * 2:
        return "court"
    if appellant_count > respondent_count + 2:
        return "appellant"
    if respondent_count > appellant_count + 2:
        return "respondent"
    if court_count >= 3 and court_count >= max(appellant_count, respondent_count):
        return "court"
    if _exp:
        return _exp
    return "neutral"


