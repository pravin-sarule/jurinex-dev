def _derive_failure_reason(j: Dict[str, Any], audit_info: Optional[Dict[str, Any]], v: Dict[str, Any]) -> str:
    st = j.get("subsequent_treatment") or {}
    overruled = st.get("overruled") or []
    if overruled:
        by = "; ".join(str(x) for x in overruled[:2])
        return f"Confirmed overruled by: {by}"

    flags = (audit_info or {}).get("hallucination_flags") or []
    if "future_year_in_citation" in flags:
        return "Citation contains a future year — likely hallucinated"
    if "placeholder_citation" in flags:
        return "Citation string is a placeholder or template value (e.g. '—', 'N/A')"
    if "suspiciously_simple_citation" in flags:
        return "Citation format is suspiciously simple and may not refer to a real judgment"

    trace = v.get("trace") or []
    for t in trace:
        if "L1:FAIL" in t:
            return "Not found in any verified source — citation does not exist in our database or Indian Kanoon"
        if "L3:FAIL" in t:
            score = v.get("match_score", 0)
            return f"Proposition misalignment — what this case says does not match how it was cited (match score: {score}%)"
        if "AUDITOR:QUARANTINED" in t:
            return "Flagged by automated verification — citation could not be confirmed in authoritative sources"

    score = v.get("match_score", 0)
    if score < 50:
        return f"Insufficient confidence ({score}%) — citation could not be verified through available sources"

    return "Verification failed — manual review required before use in court submissions"


def _compute_priority_score(j: Dict[str, Any], audit_info: Optional[Dict[str, Any]], query_volume: int = 1) -> float:
    score = 0.0
    court = (j.get("court") or "").lower()
    if "supreme court" in court:
        score += 0.4
    elif "high court" in court:
        score += 0.25
    elif "district" in court or "sessions" in court:
        score += 0.05
    else:
        score += 0.15

    year = j.get("year") or 0
    try:
        year = int(year)
    except (TypeError, ValueError):
        year = 0
    current_year = datetime.utcnow().year
    recency = max(0, 1.0 - (current_year - year) / 30.0) if year else 0
    score += recency * 0.3

    vol_bonus = min(0.2, query_volume * 0.02)
    score += vol_bonus

    conf = (audit_info or {}).get("final_confidence") or j.get("audit_confidence") or 0
    try:
        conf = int(conf)
    except (TypeError, ValueError):
        conf = 0
    if conf > 40:
        score += 0.1

    return round(min(1.0, score), 3)


def _strip_html(text: str) -> str:
    if not text:
        return ""
    text = re.sub(r"<script[^>]*>.*?</script>", " ", text, flags=re.I | re.S)
    text = re.sub(r"<style[^>]*>.*?</style>", " ", text, flags=re.I | re.S)
    text = re.sub(r"<[^>]+>", " ", text)
    return text


def _format_ik_cite_list(raw: Any) -> List[Dict[str, str]]:
    if not raw or not isinstance(raw, list):
        return []
    out = []
    for item in raw[:50]:
        if not isinstance(item, dict):
            continue
        tid = str(item.get("tid") or "")
        out.append({
            "tid":       tid,
            "title":     str(item.get("title") or ""),
            "docsource": str(item.get("docsource") or ""),
            "url":       f"https://indiankanoon.org/doc/{tid}/" if tid else "",
        })
    return out


def _looks_like_css(text: str) -> bool:
    if not text:
        return False
    sample = text[:8000]
    css_tokens = sum(sample.count(tok) for tok in (
        "{", "}", ";", ":", "var(--", "font-", "background", "border", "@keyframes"
    ))
    return css_tokens >= 12


def _clean_excerpt(text: str) -> str:
    if not text:
        return ""
    text = _strip_html(text)
    text = re.sub(r"\{[^}]{0,800}\}", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    if _looks_like_css(text):
        return ""
    return text


def _strip_html_css(text: str) -> str:
    if not text or not isinstance(text, str):
        return (text or "").strip()
    out = re.sub(r"<[^>]+>", " ", text)
    out = re.sub(r"\s+", " ", out).strip()
    lines = [ln.strip() for ln in out.split("\n") if ln.strip()]
    kept = []
    for ln in lines:
        if re.search(r"^\s*[.#\w\[\]]+\s*\{", ln) or "var(--" in ln or re.search(r"}\s*;", ln):
            continue
        if re.search(r"\b(px|em|rem|%)?\s*;?\s*}\s*$", ln) and re.search(r"[{}]", ln):
            continue
        if len(ln) > 20 and (ln.count("{") + ln.count("}") + ln.count(";")) >= 3:
            continue
        kept.append(ln)
    out = "\n".join(kept).strip()
    if not out or re.search(r"^\s*[.#]|var\(--|gradient|border-radius|box-shadow", out[:200]):
        prose_lines = [
            ln for ln in lines
            if " " in ln and not re.match(r"^\s*[.#\w-]+\s*\{", ln) and "var(" not in ln
        ]
        out = " ".join(prose_lines).strip() if prose_lines else ""
    return out[:3000] if out else ""


